                    <h1 class="center-title heiti">为什么采用倒三角</h1>
                    <div>
                        <p>
                            真城三角树采用的三角形象，来自心理学家马斯洛关于需求/动机的层次理论。按照这一理论，人的需求、动机、利益，可划分为从生理到自我实现多个层阶。三角树采用的三角是对马斯洛理论的敬意，但没有采用流行的三角模型，而是采用上宽下小的倒三角，出于以下想法：
                        </p>
                        <p>
                            人的言语和行动总是昭然醒目，但背后的物质活动、经济基础却往往不引人注目。譬如任何革命，支持其口号和行动的，都要有资金源头。一个人在球场上奔跑，在书桌前阅读，或夜空下仰望星空，都离不开平常细微的呼吸作为生命活动的基础。简单如真城，以理念立城，随处都是对精神的敬意和推崇，但丝毫离不开物质，网站需要的域名、空间需要有人来出售、有钱去购买，网站的开发、维护需要有存续的个体，而网络需要的开发技术、电脑和手机行业、网络通讯……更是涉及到欧美至少七十年的产业和就业机会。倒三角的上端和下端，上明下淡，而淡是双足、是地基。
                        </p>
                        <p>
                            人的精神活动定义了物质的价值和方向。身体是人类一切活动的基础，但两个身强体壮的古代将军，面对同样的敌人，也会有不同的资源使用策略。人的消费方向和消费结构，直接由头脑支配。一点积蓄，可以用于教育医疗投资，也可用于呼朋唤友张牙舞爪。人的投资方向，也由精神支配。同样一笔钱，在埃隆·马斯克、巴菲特、卡扎菲手上，就有不同的用途和历史影响。支配物质和身体的是人，是人的精神。
                        </p>
                        <p>
                            人的生存需求是有限的，而人的精神需求是无限的：人的安全需求是有限的，而人的进取是无限的；人的饮食需求是有限的，而人的欲望、人的求知、人的信仰、人对自由和平等的追求也都是无限的。人性本身是追求不断的，无论这种追求的指向是自我砥砺精进还是推动世界改变前进。从经济视角看人类，如果社会一直在健康运行，恩格尔系数就会持续走低，人的需求和差异就会越来越多元。
                        </p>
                        <p>
                            人的精神在不断拓展人的基础需要，个人越发展、人类整体越发展，需要的物质基础就越多。基因等前沿研究，拓展了健康需求。天文学的进步，使小行星进入人类威胁的视野。基础科学引领人类进入宇宙大航海时代，创造了难以计数的工作岗位，需要的资金更是远远超过历史上任何一个富裕奢侈的帝王。随着文明的进步，整个地球上的物质资源会越来越弥足珍贵，人类的资源需求会投向无垠的太空。
                        </p>
