                                href="http://www.ifanr.com/805057">利用「以换代修」而迫使苹果罗技亚马逊对中囶发动小型「贸易战」</a>的部分中囶人，对 WTO
                        时代的跨囶企业也没有发展共存关系的意愿，对同胞也没有共生共荣的意识，不惜影响所有人。市场契约观念的前提是大家认同共生共存，大航海初期各个国家鼓励海盗业专门袭击敌国商船就是共生体的反面。排斥共生体，符合现实，毕竟过去整个人类都是一路弱肉强食走来的，特别是在「兴，百姓苦」的地域，共生体的制度机制仍然是反向的，随着闭关锁民，情况更会有加无已。但这不代表我们要被动接受这一切，这不符合我们的生存意愿。
                    </p>
                    <p>
                        和真城有缘的朋友，更偏爱真实而不是虚假，倾向平等而非等级依附，偏爱自我完善、事业理想而非出人头地，偏爱公共责任而非「自扫门前雪」，偏爱合作而非勾心斗角。所以我们更渴望真实、平等、可信的市场，在同样的制度环境下，我们更有意愿培育市场的真实、平等、可信。
                    </p>
